int	ft_isspace(int c)
{
	unsigned char	uc;

	if (c == '\0')
		return (0);
	uc = (unsigned char)c;
	return (uc == ' '
		|| uc == '\t'
		|| uc == '\n'
		|| uc == '\v'
		|| uc == '\f'
		|| uc == '\r');
}
